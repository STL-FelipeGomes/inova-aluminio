<b>O que é a cola de silicone?</b><br>
Como o próprio nome diz, esse tipo de cola é um composto com base química no silicone, e é classificado como um adesivo monocomponente, de aparência incolor, e com um alto grau de viscosidade, ou seja, de difícil escoamento, portanto trata-se de uma cola que não é exatamente líquida, mas também não chega a formar um composto sólido.<br><br>

<b>Onde posso utilizar a cola de silicone?</b><br>
Você pode usar a cola de silicone para diversas aplicações, entretanto, como já mencionado aqui, é no artesanato que ela encontra a maioria de consumidores. Quanto aos materiais que podem ser trabalhados com esse composto estão:<br>
Isopor;<br>
Madeira;<br>
EVA (papel emborrachado)<br>
Papel<br>
Papelão;<br>
Couro;<br>
Metais;<br><br>

O bom nível de aderência entre a cola e esses materiais, permite o seu uso para diversos fins, desde trabalhos artísticos até pequenos reparos em sua casa. Além dos itens na lista existem outros substratos que respondem bem à cola de silicone, como alguns tecidos por exemplo.<br><br>

<b>Algumas dicas para usar a cola de silicone!</b><br>
As dicas para usar a cola de silicone assim como qualquer composto químico se repetem, ou seja, procure fazer o uso da cola em locais bem ventilados, evitando a inalação do odor por muito tempo. No mais, para obter um resultado interessante coma sua cola, limpe muito bem as superfícies, tirando qualquer resíduo de sujeira ou sinal de oleosidade.<br><br>

Depois de aplicar e espalhar o produto de maneira uniforme, é preciso aplicar pressão para que o tempo de cura fixe as peças. O tempo de cura inicial, é de cinco a 30 minutos dependendo da área de contato, depois disso ainda é preciso esperar cerca de 24 horas para que a fixação se dê por completo e o seu produto, seja ele qual for, esteja pronto para o uso.

